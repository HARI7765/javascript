let week = prompt("Enter the week number: ");

if (week === '1') {
  alert("Sunday");
  console.log("Sunday");
} else if (week === '2') {
  alert("Monday");
  console.log("Monday");
} else if (week === '3') {
  alert("Tuesday");
  console.log("Tuesday");
} else if (week === '4') {
  alert("Wednesday");
  console.log("Wednesday");
} else if (week === '5') {
  alert("Thursday");
  console.log("Thursday");
} else if (week === '6') {
  alert("Friday");
  console.log("Friday");
} else if (week === '7') {
  alert("Saturday");
  console.log("Saturday");
} else {
  alert("Invalid week number");
  console.log("Invalid week number");
}
